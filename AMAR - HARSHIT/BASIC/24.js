// break keywork

for (let i = 1; i <= 10; i++) {
    if (i === 4) {
        break; // Terminates the loop and the control goes out of the loop.
    }
    console.log(i);
}

// continue keyword 

for (let i = 1; i <= 10; i++) {
    if (i === 4) {
        continue; // Terminates the current iteration & continues to next iteration.
    }
    console.log(i);
}
console.log("hello there");
